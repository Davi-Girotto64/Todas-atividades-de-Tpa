package prjAula1;

public class AreaTriangulo {
	
	public static void main(String[] args) {

		//Declaração das variáveis
		double base, altura, area;
		
		//Atribuição dos valores
		base = 6;
		altura = 3;
		
		//Processamento
		area = (base * altura)/2; 
		
		//Exibição dos valores 
		System.out.println (area);
	}
}